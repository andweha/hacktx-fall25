import 'package:flutter/material.dart';
import 'package:firebase_core/firebase_core.dart';
import 'package:firebase_auth/firebase_auth.dart';
import 'firebase_options.dart'; // generated by FlutterFire
import 'onboarding_page.dart';

Future<void> main() async {
  // Ensure widgets + Firebase initialize properly
  WidgetsFlutterBinding.ensureInitialized();

  // Initialize Firebase using your project config
  await Firebase.initializeApp(
    options: DefaultFirebaseOptions.currentPlatform,
  );

  // Anonymous sign-in (each user gets a unique ID)
  await FirebaseAuth.instance.signInAnonymously();

  // Run your app as before
  runApp(const MyApp());
}

class MyApp extends StatelessWidget {
  const MyApp({super.key});

  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      title: 'Task Tracker',
      theme: ThemeData(
        colorScheme: ColorScheme.fromSeed(seedColor: Colors.deepPurple),
        useMaterial3: true,
      ),
      home: const OnboardingPage(), // keep onboarding
    );
  }
}
