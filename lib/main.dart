import 'package:flutter/material.dart';
import 'package:flutter/foundation.dart' show kIsWeb;
import 'package:firebase_core/firebase_core.dart';
import 'package:firebase_auth/firebase_auth.dart';
import 'package:cloud_firestore/cloud_firestore.dart';
import 'firebase_options.dart'; // generated by FlutterFire
import 'main_navigation.dart';

Future<void> main() async {
  WidgetsFlutterBinding.ensureInitialized();
  await Firebase.initializeApp(
    options: DefaultFirebaseOptions.currentPlatform,
  );

  // Ensure an authenticated user exists
  await FirebaseAuth.instance.signInAnonymously();

  // Create profile doc if missing
  await _ensureProfile();

  runApp(const MyApp());
}

Future<void> _ensureProfile() async {
  final user = FirebaseAuth.instance.currentUser;
  if (user == null) return;

  final ref =
      FirebaseFirestore.instance.collection('user_profiles').doc(user.uid);
  final snap = await ref.get();
  if (snap.exists) return;

  // Simple default profile
  final uname = _genUsername();
  await ref.set({
    'displayName': uname,
    'username': uname,
    'photoURL': null,
    'createdAt': FieldValue.serverTimestamp(),
    'anon': user.isAnonymous,
    'friendUids': [],
    'prefs': {},
  });
}

String _genUsername() {
  const chars = 'abcdefghijklmnopqrstuvwxyz0123456789';
  final now = DateTime.now().microsecondsSinceEpoch;
  // cheap, deterministic-ish suffix
  final suffix = List.generate(4, (i) => chars[(now >> (i * 5)) % chars.length])
      .join();
  return 'user-$suffix';
}

class MyApp extends StatelessWidget {
  const MyApp({super.key});

  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      title: 'Task Tracker',
      theme: ThemeData(
        colorScheme: ColorScheme.fromSeed(seedColor: Colors.deepPurple),
        useMaterial3: true,
      ),
      home: kIsWeb ? const _WebAppWrapper() : const MainNavigation(),
    );
  }
}

class _WebAppWrapper extends StatelessWidget {
  const _WebAppWrapper();

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      backgroundColor: Colors.grey[200],
      body: Center(
        child: Container(
          constraints: const BoxConstraints(
            minWidth: 375,  // Minimum mobile width
            maxWidth: 1200, // Maximum desktop width
            minHeight: 600, // Minimum height
            maxHeight: 800, // Maximum height
          ),
          decoration: BoxDecoration(
            borderRadius: BorderRadius.circular(12),
            boxShadow: [
              BoxShadow(
                color: Colors.black.withOpacity(0.1),
                blurRadius: 20,
                offset: const Offset(0, 10),
              ),
            ],
          ),
          clipBehavior: Clip.antiAlias,
          child: const MainNavigation(),
        ),
      ),
    );
  }
}
